package challenge.domain.order

import challenge.domain.adress.Address
import challenge.domain.customer.Customer
import challenge.domain.payment.Payment
import challenge.domain.payment.PaymentMethod
import challenge.domain.product.Product
import java.lang.Exception
import java.util.*

class Order(val customer: Customer, val address: Address) {
    private val items = mutableListOf<OrderItem>()
    var closedAt: Date? = null
        private set
    var payment: Payment? = null
        private set
    val totalAmount
        get() = items.sumByDouble { it.total }

    fun addProduct(product: Product, quantity: Int) {
        var productAlreadyAdded = items.any { it.product == product }
        if (productAlreadyAdded)
            throw Exception("The product have already been added. Change the amount if you want more.")

        items.add(OrderItem(product, quantity))
    }

    fun pay(method: PaymentMethod) {
        if (payment != null)
            throw Exception("The order has already been paid!")

        if (items.count() == 0)
            throw Exception("Empty order can not be paid!")

        payment = Payment(this, method)

        close()
    }

    private fun close() {
        closedAt = Date()
    }

    fun checkout() {
        items.forEach { i ->
            val strategy = i.product.getStrategy()
            strategy.checkout(i.product, this.customer)
        }
    }
}